			Bonjour à toutes et à tous ! Je suis <em>Le Duc</em>, aka Antoine Ludwig, créateur, programmateur et animateur des émissions <em>Rockin' Auxois</em> et <em>Metalesia</em> sur la radio <a href="http://www.auxois-fm.fr">Auxois FM</a>.<br>
			Pour la petite histoire, j'étais auparavant chroniqueur dans l'émission de metal extrème Black Roots Metallistic sur Radio Campus Dijon et l'idée de créer ma propre émission avec un contenu plus diversifié et accessible a commencé à me titiller courant 2019. Souhaitant mêler plusieurs styles musicaux qui me sont chers sans pour autant complètement abandonner le metal bien bourrin, j'en suis arrivé à la conclusion que pour plus de clarté il me fallait non pas une mais deux émissions. Une fois l'idée trouvée de faire une émission assez soft et une plus violente, j'ai été démarcher <em>Gaetan</em> et <em>Manou</em> d'<em>Auxois FM</em> qui m'ont laissé ma chance en tentant de masquer au mieux leur scepticisme. Il faut dire que le <em>rock</em> et le <em>metal</em>, c'est pas la marque de fabrique habituelle d'<em>Auxois FM</em> qui programme plutôt des musiques actuelles et urbaines.L'aventure <em>Rockin'Auxois</em> et <em>Metalesia</em> a donc débuté en septembre 2019 avec pour consigne de passer de la musique pouvant convenir à une écoute dans les restaurants partenaires du coin au moins jusqu'à 21h.<br>
			Deux heures d'émission c'est un peu plus d'1h30 de musique chaque semaine, et au début, ce n'est pas simple de réussir à proposer autant de contenu chaque semaine, surtout que je tiens absolument à varier les styles, à proposer des groupes ou chansons pas forcément très connus ou des versions live intéressantes. Et pour celà, je mets un point d'honneur à écouter en entier au minimum une fois la playlist avant de la diffuser. Heureusement, j'ai eu la chance de voir arriver rapidement un assistant pour m'aider dans mes recherches et mes écoutes. Mon fils, Arthur dit Tutur Le Dur pour les <em>trve warriors of metal of steel</em> est né le 13 octobre 2019 et a eu la bonne idée de ne pas vouloir dormir la nuit (et la plupart des journées) sans avoir sa dose de musique. J'ai donc passé des nuits avec lui à écouter toutes sortes de musiques, ses préférences allant plutôt vers le <em>power metal allemand à base de dinosaures laser</em> et vers le <em>thrash metal neuneu</em>. Il m'a donc beaucoup aidé pour la programmation des émissions et à ce jour j'ai encore énormément de morceaux pas encore diffusés que j'ai listé pendant cette période.<br>
			Pour le contenu des émissions, les podcasts et les diverses infos, je vous laisse farfouiller sur le site. Et n'oubliez pas qu'un <em>petit mot gentil</em>via l'onglet contact ou sur Facebook, ça fait toujours <em>super plaisir</em> !
